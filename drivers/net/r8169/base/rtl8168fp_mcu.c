/* ------------------------------------PHY 8168FP------------------------------------- */

void
hw_mac_mcu_config_8168fp(struct rtl_hw *hw)
{
	if (hw->NotWrMcuPatchCode)
		return;

	switch (hw->mcfg) {
	case CFG_METHOD_31:
		rtl8168_set_mac_mcu_8168fp_1(hw);
		break;
	case CFG_METHOD_32:
		rtl8168_set_mac_mcu_8168fp_2(hw);
		break;
	case CFG_METHOD_33:
	case CFG_METHOD_34:
		rtl8168_set_mac_mcu_8168fp_3(hw);
		break;
	}
}

